package myVelib;
public class GPS {
	private float x,y;

	public GPS(float x, float y) {
		super();
		this.x = x;
		this.y = y;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	@Override
	public String toString() {
		return "GPS [x=" + x + ", y=" + y + "]";
	}
	
	
	
}